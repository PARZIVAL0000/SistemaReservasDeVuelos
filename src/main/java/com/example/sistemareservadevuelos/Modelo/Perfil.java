package com.example.sistemareservadevuelos.Modelo;

/**
 *
 * @author denni
 */
public class Perfil {
    private int id;
    private String perfil;
    private int estado;
    
    public Perfil(){
        
    }
    
    public Perfil(int id, String perfil, int estado){
        this.id = id;
        this.perfil = perfil;
        this.estado = estado;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the perfil
     */
    public String getPerfil() {
        return perfil;
    }

    /**
     * @param perfil the perfil to set
     */
    public void setPerfil(String perfil) {
        this.perfil = perfil;
    }

    /**
     * @return the estado
     */
    public int getEstado() {
        return estado;
    }

    /**
     * @param estado the estado to set
     */
    public void setEstado(int estado) {
        this.estado = estado;
    }
    
    
}
